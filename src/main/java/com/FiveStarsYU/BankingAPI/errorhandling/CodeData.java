package com.FiveStarsYU.BankingAPI.errorhandling;

public class CodeData {

    private int code;
    private Object data;

    public CodeData(int code, Object data) {
        this.code = code;
        this.data = data;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public Object getData() {
        return data;
    }

    public void setData(Object data) {
        this.data = data;
    }
}
